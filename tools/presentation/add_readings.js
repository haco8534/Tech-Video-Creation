const CMD_REGEX = [
  [/\bls\b/g, 'エルエス'],
  [/\bgrep\b/g, 'グレップ'],
  [/\bsed\b/g, 'セド'],
  [/\bawk\b/g, 'オーク'],
  [/\bcurl\b/g, 'カール'],
  [/\bcp\b/g, 'シーピー'],
  [/\bdir\b/g, 'ディレ'],
  [/\bcopy\b/g, 'コピー'],
  [/\bssh\b/g, 'エスエスエイチ'],
  [/\bfindstr\b/g, 'ファインドストリング'],
  [/\bC言語/g, 'シー言語'],
  [/\bCが/g, 'シーが'],
  [/\bCで/g, 'シーで'],
  [/\bCの/g, 'シーの'],
  [/\bCと/g, 'シーと'],
  [/\bCから/g, 'シーから'],
  [/\bCや/g, 'シーや'],
  [/\bC並み/g, 'シーなみ'],
  [/\bCって/g, 'シーって'],
  [/\bC？/g, 'シー？'],
  [/\bC、/g, 'シー、'],
  [/\bMIT/g, 'エムアイティー'],
  [/(\d+)W\b/g, '$1ワット'],
];

function applyReading(text) {
  let r = text;

  // JP_DICT: 日本語の誤読パターンを先にひらがなに開く（複合語単位）
  for (const [search, replace] of JP_DICT) {
    if (r.includes(search)) {
      r = r.split(search).join(replace);
    }
  }

  // まずプレースホルダ置換（長い順に）
  const placeholders = [];
  for (let i = 0; i < DICT.length; i++) {
    const [search, replace] = DICT[i];
    const ph = `\x00${i}\x00`;
    if (r.includes(search)) {
      r = r.split(search).join(ph);
      placeholders.push([ph, replace]);
    }
  }
  // プレースホルダをカタカナに戻す
  for (const [ph, replace] of placeholders) {
    r = r.split(ph).join(replace);
  }

  // コマンド系（正規表現）
  for (const [re, replace] of CMD_REGEX) {
    r = r.replace(re, replace);
  }

  // 数値パーセント
  r = r.replace(/(\d+)%→(\d+)%/g, '$1パーセントから$2パーセント');
  r = r.replace(/(\d+)%/g, '$1パーセント');

  // → を「、」に
  r = r.replace(/→/g, '、');

  return r;
}

let modified = 0;
for (const scene of map.scenes) {
  for (const line of scene.lines) {
    // 既存のreadingを削除
    delete line.reading;

    const text = line.text;
    const hasJpDict = JP_DICT.some(([k]) => text.includes(k));
    if (/[a-zA-Z]/.test(text) || /[%→]/.test(text) || hasJpDict) {
      const reading = applyReading(text);
      if (reading !== text) {
        line.reading = reading;
        modified++;
      }
    }
  }
}

// ===== 未登録英単語の自動検出 =====
// DICT置換後に残った英単語（2文字以上）を検出する
function detectUnregisteredTokens(text) {
  // DICT置換をシミュレート（プレースホルダを空文字に）
  let r = text;
  for (let i = 0; i < DICT.length; i++) {
    const [search] = DICT[i];
    if (r.includes(search)) {
      r = r.split(search).join('');
    }
  }
  // CMD_REGEX置換もシミュレート
  for (const [re] of CMD_REGEX) {
    r = r.replace(re, '');
  }
  // 残った英単語トークンを抽出（2文字以上）
  const remaining = r.match(/[a-zA-Z][a-zA-Z0-9.#+\-]{1,}/g) || [];
  return remaining;
}

const unregistered = new Map(); // token → 出現箇所リスト
for (const scene of map.scenes) {
  for (const line of scene.lines) {
    const tokens = detectUnregisteredTokens(line.text);
    for (const token of tokens) {
      if (!unregistered.has(token)) unregistered.set(token, []);
      unregistered.get(token).push(`scene ${scene.id}: "${line.text}"`);
    }
  }
}

if (unregistered.size > 0) {
  console.error(`\n❌ 未登録の英単語が ${unregistered.size} 件見つかりました。DICTに追加してください:\n`);
  for (const [token, locations] of unregistered) {
    console.error(`  ⚠️  "${token}"`);
    console.error(`      例: ${locations[0]}`);
  }
  console.error(`\nadd_readings.js の DICT に上記の単語とカタカナ読みを追加してください。`);
  console.error('読み間違いを防ぐため、未登録の英単語がある状態では処理を中断します。\n');
  process.exit(1);
}

